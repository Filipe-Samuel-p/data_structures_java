package LE1.ex2;

public class Conjunto {

    private int elemento;
    private int tamanhoConj;

    public Conjunto(int elemento, int tamanhoConj) {
        this.elemento = elemento;
        this.tamanhoConj = tamanhoConj;
    }

    public int getElemento() {
        return elemento;
    }

    public void setElemento(int elemento) {
        this.elemento = elemento;
    }

    public int getTamanhoConj() {
        return tamanhoConj;
    }

    public void setTamanhoConj(int tamanhoConj) {
        this.tamanhoConj = tamanhoConj;
    }




}
